// inheritance:
// an OOP principle where you can define a hierarchical relationship amongst classes
// a base class AKA super class AKA parent class
// a derived class AKA sub class AKA child class
// a sub class inherits the state and behavior from a super class
// in swift, struct do not support inheritance
// in swift, there is no universal base class
// for example there is no object like there is in java
// use the kwyword super to refer to a member of the super class
// use the keyword override to override the behavior or the state in a subclass
// you can only inherit from one super class in swift
